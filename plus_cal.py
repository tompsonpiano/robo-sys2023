import sys
ans = 0

def plus(x, y):
    x + y
    return x + y

def minus(x, y):
    x - y
    return x - y

def multiply(x, y):
    x * y
    return x * y

def divide(x, y):
    x / y
    return x / y

num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

op = input("Choose operator(+,-,*,/): ")

if op == '+':
    ans = plus(num1,num2)
    print(ans)

elif op == '-':
    ans = minus(num1,num2)
    print(ans)
    
elif op == '*':
    ans = multiply(num1,num2)
    print(ans)
    
elif op == '/':
    ans = divide(num1,num2)
    print(ans)
    
else:
    print("Please try again")